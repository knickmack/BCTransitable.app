#import "UIColor+BCTransitable.h"

@implementation UIColor (BCTransitable)

+ (UIColor *)bctransitableBlueColor {
    return [UIColor colorWithRed:0.f green:75/255.f blue:141/255.f alpha:1.f];
}

+ (UIColor *)bctransitableGreenColor {
    return [UIColor colorWithRed:84/255.f green:185/255.f blue:72/255.f alpha:1.f];
}

@end
